// 14. Do..While Loop

package inspiron;

public class DoWhileLoop {

	public static void main(String[] args) 
	{
		int x = 1; 
		 
		 do 
		 { 
			 System.out.print("value of x : " + x ); 
			 x++; 
			 System.out.print("\n"); 
		 }
		 while( x < 11 );

	}

}

