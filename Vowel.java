// 7. Given a Character check whether it’s a vowel or not.
package inspiron;

public class Vowel {

	public static void main(String[] args) {
		char ch = 'i'; 
		 if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' ) 
		 System.out.println(ch + " is vowel"); 
		 else
		 System.out.println(ch + " is consonant");

	}

}

/*output:
i is vowel*/